package Presentation;

import javax.swing.*;

/**
 * The OperationCorrect class represents a simple panel indicating a successful operation.
 */
public class OperationCorrect {
    /**
     * The `JPanel` representing the operationCorrect panel.
     */
    private JPanel operationCorrect;

    /**
     * Gets the JPanel associated with this OperationCorrect instance.
     *
     * @return The JPanel representing the successful operation panel.
     */
    public JPanel getOperationCorrect() {
        return operationCorrect;
    }
}